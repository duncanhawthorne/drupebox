def note(text):
    """Prints a note to the console."""
    print(">>>>>>", text)


def alert(text):
    """Prints an alert to the console."""
    print("!!!!!!", text)


def fyi(text):
    """Prints a message for the user's information."""
    print("      ", text)


def fyi_ignore(text):
    """Prints a message about an ignored item."""
    print("        -> ignore", text)
